class MyHashMap:
    def __init__(self):              
        map= []
        def put(self, key: int, value: int) -> None:
            map.insert[key,value]    
        def get(self, key: int) -> int:
            return map.index(key)
        def remove(self, key: int) -> None:
            map.remove(key)


